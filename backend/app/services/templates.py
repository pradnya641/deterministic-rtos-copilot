# === UART ====================================================================
LPC2148_UART_TEMPLATE = """\
#include <lpc214x.h>

void UART{channel}_Init(void)
{{
    /* Step 1: Configure pins for UART{channel} */
    {pinsel_reg} &= ~({pinsel_mask} << {pinsel_shift});
    {pinsel_reg} |=  ({pinsel_val} << {pinsel_shift});

    /* Step 2: Enable DLAB */
    U{channel}LCR = 0x83;

    /* Step 3: Set Baud Rate ({baud})
       Assumes PCLK = 15MHz (VPB_DIV=4, CCLK=60MHz)
       DLL = PCLK / (16 * baud) = {dll}
       If your PCLK differs, recalculate: DLL = PCLK_Hz / (16 * baud) */
    U{channel}DLL = {dll};
    U{channel}DLM = {dlm};

    /* Step 4: 8N1, Disable DLAB */
    U{channel}LCR = 0x03;
    U{channel}FCR = 0x07;
}}"""

def get_uart_config(channel: int, baud: int):
    # PCLK = CCLK / VPB_DIV. Default VPB_DIV=4, CCLK=60MHz -> PCLK=15MHz
    # If VPB_DIV=1, PCLK=60MHz. Adjust PCLK_HZ if your system differs.
    PCLK_HZ = 15_000_000
    baud_divisors = {b: round(PCLK_HZ / (16 * b)) for b in [9600, 19200, 38400, 57600, 115200]}
    dll = baud_divisors.get(baud, round(PCLK_HZ / (16 * baud)))
    
    mapping = {
        0: {"reg": "PINSEL0", "shift": 0, "mask": 0xF, "val": 0x5},
        1: {"reg": "PINSEL0", "shift": 16, "mask": 0xF, "val": 0x5},
    }
    m = mapping.get(channel, mapping[0])
    
    return LPC2148_UART_TEMPLATE.format(
        channel=channel, baud=baud, dll=dll, dlm=0,
        pinsel_reg=m["reg"], pinsel_shift=m["shift"], 
        pinsel_mask=m["mask"], pinsel_val=m["val"]
    )

LPC2148_UART_REGISTER_TABLE = "| Register | Role | Description |\n|---|---|---|\n| UxLCR | Line Control | DLAB and Frame format |\n| UxDLL/M | Divisor Latch | Baud rate generation |\n| UxLSR | Line Status | Ready checks |"

