# === SPI =====================================================================
LPC2148_SPI_TEMPLATE = """\
#include <lpc214x.h>

void SPI_Init(void)
{{
    /* Step 1: Configure P0.4=SCK0, P0.5=MISO0, P0.6=MOSI0, P0.7=SSEL0 */
    /* PINSEL0 bits [9:8]=01 (SCK), [11:10]=01 (MISO), [13:12]=01 (MOSI), [15:14]=01 (SSEL) */
    PINSEL0 = (PINSEL0 & ~(0xFF << 8)) | (0x55 << 8);

    /* Step 2: Configure SPI — Master, 8-bit, CPOL=0, CPHA=0
       S0SPCR bit layout (LPC2148 UM10139, Chapter 16):
         Bit  2 (BitEnable) = 1  -> use BITS[11:8] field for transfer size
         Bit  5 (MSTR)      = 1  -> Master mode
         Bits 11:8 (BITS)   = 8  -> 8-bit transfer size (binary 1000)
       Combined: (1<<5)|(1<<2)|(8<<8) = 0x824
       Note: if BitEnable (bit 2) = 0, BITS[11:8] is IGNORED and
             hardware defaults to 8-bit regardless. Always set bit 2
             explicitly to avoid silent misconfiguration. */
    S0SPCR = (1 << 5) | (1 << 2) | (8 << 8); /* 0x824: Master, BitEnable, 8-bit */

    /* Step 3: Set clock divider — SPI clock = PCLK / S0SPCCR */
    /* At PCLK=15MHz, S0SPCCR=8 gives SPI clock = 1.875MHz */
    /* S0SPCCR must be an even number >= 8 (datasheet constraint) */
    S0SPCCR = 8;
}}

uint8_t SPI_Transfer(uint8_t data)
{{
    S0SPDR = data;                    /* Write byte to start transfer */
    while (!(S0SPSR & (1 << 7)));    /* Wait for SPIF (transfer complete, bit 7) */
    return S0SPDR;                    /* Read received byte */
}}"""

def get_spi_config():
    return LPC2148_SPI_TEMPLATE

LPC2148_SPI_REGISTER_TABLE = """\
| Register | Address    | Role                                      |
|----------|------------|-------------------------------------------|
| PINSEL0  | 0xE002C000 | Pin function select (SCK/MISO/MOSI/SSEL)  |
| S0SPCR   | 0xE0020000 | SPI Control Register (master/slave, bits) |
| S0SPSR   | 0xE0020004 | SPI Status Register (SPIF flag)           |
| S0SPDR   | 0xE0020008 | SPI Data Register (TX/RX)                 |
| S0SPCCR  | 0xE002000C | SPI Clock Counter Register (clock divider)|"""

# === I2C =====================================================================
LPC2148_I2C_TEMPLATE = """\
#include <lpc214x.h>

void I2C_Init(void)
{{
    /* Step 1: Configure P0.2=SDA0, P0.3=SCL0 */
    /* PINSEL0 bits [5:4]=01 (SDA), [7:6]=01 (SCL) */
    PINSEL0 = (PINSEL0 & ~(0xF << 4)) | (0x5 << 4);

    /* Step 2: Set I2C clock — 100kHz at PCLK=15MHz */
    /* I2SCLH + I2SCLL = PCLK / I2C_freq = 15000000 / 100000 = 150 */
    I20SCLH = 75;
    I20SCLL = 75;

    /* Step 3: Enable I2C interface */
    I20CONSET = (1 << 6);   /* I2EN bit */
}}

uint8_t I2C_ReadByte(uint8_t dev_addr, uint8_t reg_addr)
{{
    /* START */
    I20CONSET = (1 << 5);           /* STA bit */
    while (!(I20CONSET & (1 << 3))); /* Wait SI */

    /* Send device address + WRITE */
    I20DAT = (dev_addr << 1) | 0;
    I20CONCLR = (1 << 5);           /* Clear STA */
    while (!(I20CONSET & (1 << 3)));

    /* Send register address */
    I20DAT = reg_addr;
    I20CONCLR = (1 << 3);           /* Clear SI */
    while (!(I20CONSET & (1 << 3)));

    /* Repeated START */
    I20CONSET = (1 << 5);
    while (!(I20CONSET & (1 << 3)));

    /* Send device address + READ */
    I20DAT = (dev_addr << 1) | 1;
    I20CONCLR = (1 << 5) | (1 << 3);
    while (!(I20CONSET & (1 << 3)));

    /* Read byte (NACK to signal last byte) */
    I20CONCLR = (1 << 2) | (1 << 3); /* AA=0 (NACK), clear SI */
    while (!(I20CONSET & (1 << 3)));

    uint8_t data = I20DAT;

    /* STOP */
    I20CONSET = (1 << 4);           /* STO bit */
    return data;
}}"""

def get_i2c_config():
    return LPC2148_I2C_TEMPLATE

# === GPIO ====================================================================
LPC2148_GPIO_TEMPLATE = """\
#include <lpc214x.h>

void GPIO_Setup(void)
{{
    /* Configure {pin} as Output */
    IO{port}DIR |= (1 << {pin_num});
}}"""

def get_gpio_config(pin_str: str):
    # Parse "P0.10"
    try:
        parts = pin_str.replace("P", "").split(".")
        port = parts[0]
        pin_num = parts[1]
        return LPC2148_GPIO_TEMPLATE.format(pin=pin_str, port=port, pin_num=pin_num)
    except:
        return "// Invalid Pin Format"
