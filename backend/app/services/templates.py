# === PWM =====================================================================
LPC2148_PWM_TEMPLATE = """\
#include <lpc214x.h>

void PWM_Init(void)
{{
    /* Step 1: Select {func} function on {pin} */
    {pinsel_reg} = ({pinsel_reg} & ~({pinsel_mask} << {pinsel_shift})) | ({pinsel_val} << {pinsel_shift});
    PWMPR = 0;
    PWMMR0 = 1000;
    PWMMR{channel} = {duty_ticks};
    PWMMCR = (1 << 1);
    PWMPCR = (1 << {pcr_bit});
    PWMLER = (1 << 0) | (1 << {channel});
    PWMTCR = (1 << 0) | (1 << 3);
}}"""

def get_pwm_config(channel: int, duty_percent: int):
    # Pin Mapping Gold Data
    mapping = {
        1: {"pin": "P0.0", "reg": "PINSEL0", "shift": 0, "mask": 0x3, "val": 0x2, "pcr": 9},
        2: {"pin": "P0.7", "reg": "PINSEL0", "shift": 14, "mask": 0x3, "val": 0x2, "pcr": 10},
        5: {"pin": "P0.21", "reg": "PINSEL1", "shift": 10, "mask": 0x3, "val": 0x1, "pcr": 13},
    }
    m = mapping.get(channel, mapping[1])
    duty_ticks = int(1000 * (duty_percent / 100))
    
    return LPC2148_PWM_TEMPLATE.format(
        func=f"PWM{channel}", pin=m["pin"], pinsel_reg=m["reg"],
        pinsel_shift=m["shift"], pinsel_mask=m["mask"], pinsel_val=m["val"],
        channel=channel if channel in [1,2,3,4,5,6] else 1,
        duty_ticks=duty_ticks, pcr_bit=m["pcr"]
    )

